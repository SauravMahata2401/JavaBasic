package pkg.Operators;

public class AssignmentOperation {
	
	public static void main(String[] args) {
		
		int x,y;
		x=30;
		y=10;
		x++;
		y = y +6;
		System.out.println("Answer is: "+x++);
		System.out.println("Answer is: "+x);
		System.out.println("Answer is: "+y);
		
		int z=20;
		z *= 5; // z = z*5;
		System.out.println("Value is: "+z);
	
	}

}
