package pkg.Operators;

public class MathOperation {
	
	public static void main(String[] args) {
		
		int x,y;
		x=30;
		y=10;
		int add = x+y;
		System.out.println("Answer is: "+add);
	
		int sub = x-y;
		System.out.println("Substraction value is: "+sub);
		
		int mul = x * y;
		System.out.println("Multiplication value is: "+mul);
		
		int div = x / y;
		System.out.println("Divition value is: "+div);
		
		int rem = x % y;
		System.out.println("Remender value is: "+rem);
	}

}
